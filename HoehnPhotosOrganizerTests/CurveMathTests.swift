    /// and the combined sum stays within a reasonable range before ink limiting.
    func test_inkOverlap_twoChannels_sumNeverExceeds100() {
        // K channel: linear ramp to full density
        let kChannel = CurveLabFixtures.testInkChannelLinear(name: "K", maxValue: 65535)
        // LK channel: bell curve centered at midtones
        let lkChannel = CurveLabFixtures.testInkChannelBellCurve(name: "LK", peak: 100, width: 50.0)

        for i in 0..<256 {
            let kNorm = Double(kChannel.values[i]) / 65535.0
            let lkNorm = Double(lkChannel.values[i]) / 65535.0
            // Verify each individual channel is in [0, 1]
            XCTAssertLessThanOrEqual(kNorm, 1.0, "K normalized at \(i) must be <= 1.0")
            XCTAssertLessThanOrEqual(lkNorm, 1.0, "LK normalized at \(i) must be <= 1.0")
            XCTAssertGreaterThanOrEqual(kNorm, 0.0, "K normalized at \(i) must be >= 0.0")
            XCTAssertGreaterThanOrEqual(lkNorm, 0.0, "LK normalized at \(i) must be >= 0.0")
        }

        // With a half-strength K, the raw channel sum can exceed 1.0 where the
        // bell curve peaks (this is expected before ink-limiting is applied).
