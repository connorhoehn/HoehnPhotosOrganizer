        let exported = exportCurveTxt(steps: steps)
        let lines = exported.components(separatedBy: "\n")
        let dataLines = lines.filter { !$0.hasPrefix("#") && !$0.isEmpty }

        for (i, line) in dataLines.enumerated() {
            guard let value = Int(line) else {
                XCTFail("Line \(i) is not an integer: '\(line)'")
                continue
            }
            XCTAssertGreaterThanOrEqual(value, 0, "Value at line \(i) should be >= 0")
            XCTAssertLessThanOrEqual(value, 255, "Value at line \(i) should be <= 255")
        }
    }

