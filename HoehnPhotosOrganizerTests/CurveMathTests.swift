/// Replicates the CurveBuilderView interpolation algorithm (private in production).
/// Linear interpolation over sorted CurveStep array for a given t in [0,1].
private func interpolate(t: Double, steps: [CurveStep]) -> Double {
    guard !steps.isEmpty else { return t }
    if t <= steps.first!.input { return steps.first!.output }
    if t >= steps.last!.input  { return steps.last!.output  }

    for i in 0..<(steps.count - 1) {
        let lo = steps[i]
        let hi = steps[i + 1]
        if t >= lo.input && t <= hi.input {
            let span = hi.input - lo.input
            guard span > 0 else { return lo.output }
            let fraction = (t - lo.input) / span
            return lo.output + fraction * (hi.output - lo.output)
        }
    }
    return steps.last!.output
}

/// Apply gamma curve: pow(value, gamma), clamped to [0, 1].
private func applyGamma(_ value: Double, gamma: Double) -> Double {
    guard value >= 0 else { return 0 }
    guard value <= 1 else { return 1 }
    return pow(value, gamma)
}

/// Blend two UInt16 arrays (256 entries each) using zone weights.
/// weights is a SplitToneConfig-style struct with highlights/midtones/shadows (0-100).
/// Shadows = input indices 0-84, Midtones = 85-169, Highlights = 170-255.
/// weight values represent how much of curve1 to use (100 = all curve1, 0 = all curve2).
private func blendCurves(
    curve1: [UInt16],
    curve2: [UInt16],
    shadowWeight: Double,
    midtoneWeight: Double,
    highlightWeight: Double
) -> [UInt16] {
    precondition(curve1.count == 256 && curve2.count == 256)
    var result = [UInt16](repeating: 0, count: 256)
    for i in 0..<256 {
        let weight: Double
        if i < 85 {
            weight = shadowWeight / 100.0
        } else if i < 170 {
            weight = midtoneWeight / 100.0
        } else {
            weight = highlightWeight / 100.0
        }
        let blended = Double(curve1[i]) * weight + Double(curve2[i]) * (1.0 - weight)
        result[i] = UInt16(min(max(blended + 0.5, 0), 65535))
    }
    return result
}

/// Replicate the CurveBuilderView export logic: CurveStep array -> QTR .txt string.
private func exportCurveTxt(steps: [CurveStep]) -> String {
    var lines: [String] = []
    lines.append("# QTR Linearization Curve")
    lines.append("# Generated by HoehnPhotos CurveLab")
    lines.append("# Values: 256 ink density steps (0 = no ink, 255 = full ink)")
    lines.append("")

    if steps.isEmpty {
        for i in 0..<256 {
            lines.append("\(i)")
        }
    } else {
        let sorted = steps.sorted(by: { $0.input < $1.input })
        for i in 0..<256 {
            let t = Double(i) / 255.0
            let output = interpolate(t: t, steps: sorted)
            let value = Int(output * 255.0 + 0.5)
            lines.append("\(min(max(value, 0), 255))")
        }
    }

    return lines.joined(separator: "\n")
}

// MARK: - Fixtures

/// Fixtures for CurveMath tests, matching the CurveLabFixtures API.
private enum CurveLabFixtures {

    /// Evenly spaced linear steps from (0,0) to (1,1).
    static func testLinearCurveSteps(count: Int) -> [CurveStep] {
        guard count > 1 else {
            return count == 1 ? [CurveStep(input: 0.5, output: 0.5)] : []
        }
        return (0..<count).map { i in
            let t = Double(i) / Double(count - 1)
            return CurveStep(input: t, output: t)
        }
    }

    /// S-curve steps: compressed shadows/highlights, steeper midtones.
    /// Uses a sigmoid-like remapping: output = 0.5 + 0.5 * tanh(3 * (input - 0.5))
    /// normalized to 0-1.
    static func testSCurveCurveSteps(count: Int) -> [CurveStep] {
        guard count > 1 else {
            return count == 1 ? [CurveStep(input: 0.5, output: 0.5)] : []
        }
        return (0..<count).map { i in
            let input = Double(i) / Double(count - 1)
            // Sigmoid S-curve centered at 0.5
            let raw = 1.0 / (1.0 + exp(-6.0 * (input - 0.5)))
            // Normalize so endpoints are exactly 0 and 1
            let rawMin = 1.0 / (1.0 + exp(-6.0 * (0.0 - 0.5)))
            let rawMax = 1.0 / (1.0 + exp(-6.0 * (1.0 - 0.5)))
            let output = (raw - rawMin) / (rawMax - rawMin)
            return CurveStep(input: input, output: output)
        }
    }

    /// Linear ramp ink channel: values go from 0 to maxValue across 256 entries.
    static func testInkChannelLinear(name: String = "K", maxValue: UInt16 = 65535) -> InkChannel {
        let values = (0..<256).map { i in
            UInt16(Double(i) / 255.0 * Double(maxValue) + 0.5)
        }
        return InkChannel(name: name, values: values)
    }

    /// Empty ink channel: all 256 values are zero.
    static func testInkChannelEmpty(name: String = "Empty") -> InkChannel {
        InkChannel(name: name, values: [UInt16](repeating: 0, count: 256))
    }

    /// Bell curve ink channel: peaks at a given index, tapers by Gaussian width.
    static func testInkChannelBellCurve(name: String = "LK", peak: Int = 128, width: Double = 40.0) -> InkChannel {
        let values = (0..<256).map { i in
            let dist = Double(i - peak)
            let gauss = exp(-(dist * dist) / (2.0 * width * width))
            return UInt16(gauss * 65535.0 + 0.5)
        }
        return InkChannel(name: name, values: values)
    }
}

