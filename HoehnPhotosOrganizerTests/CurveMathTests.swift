    /// Highlight blocking density at 49: values below that density get zeroed
    func test_blockingDensity_clipsHighlights() {
        let channel = CurveLabFixtures.testInkChannelLinear(name: "K", maxValue: 65535)
        let blockingThreshold: UInt16 = 49  // UInt16 value threshold

        // Apply blocking: zero out any values below the threshold
        let blocked = channel.values.map { v -> UInt16 in
            v < blockingThreshold ? 0 : v
        }

        // Low-density entries (near paper white) should be zeroed
        XCTAssertEqual(blocked[0], 0, "Index 0 should be blocked (below threshold)")

        // The threshold corresponds to input index where value first reaches 49
        // Linear ramp: value[i] = round(i/255 * 65535)
        // value[0] = 0, so several low indices will be below 49
        for i in 0..<256 {
            if channel.values[i] < blockingThreshold {
                XCTAssertEqual(blocked[i], 0,
                               "Index \(i) with value \(channel.values[i]) should be blocked")
            } else {
                XCTAssertEqual(blocked[i], channel.values[i],
                               "Index \(i) above threshold should be unchanged")
            }
        }

        // High-density entries should be unaffected
        XCTAssertEqual(blocked[255], channel.values[255],
                       "Max density should not be blocked")
    }

