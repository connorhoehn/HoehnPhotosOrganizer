    // MARK: - Helpers

    /// Serialize a QTRQuadFile back to .quad format string.
    private func serializeQuadFile(_ quad: QTRQuadFile) -> String {
        var lines: [String] = []
        lines.append("## QuadToneRIP K,C,M,Y,LC,LM,LK,LLK")
        // Re-emit non-header comments, but skip channel marker lines
        // (they will be re-added below for each channel section)
        for comment in quad.comments where !comment.starts(with: "##") {
            let lower = comment.lowercased()
            let isChannelMarker = lower.contains("curve")
                && !lower.contains("linearized")
                && !lower.contains("max")
            if !isChannelMarker {
                lines.append(comment)
            }
        }
        let channelNames = ["K", "C", "M", "Y", "LC", "LM", "LK", "LLK"]
        for (i, channel) in quad.channels.enumerated() {
            lines.append("# \(channelNames[i]) curve")
            for value in channel.values {
                lines.append("\(value)")
            }
        }
        return lines.joined(separator: "\n")
    }

    /// Generate a K-only channel with a gamma curve applied.
    private func generateGammaKChannel(gamma: Double, maxValue: UInt16 = 29695) -> [UInt16] {
        (0..<256).map { i in
            let t = Double(i) / 255.0
            let v = pow(t, gamma)
            return UInt16(min(Double(maxValue) * v, Double(UInt16.max)))
        }
    }

    /// Build a full .quad string with only K active, using the given K values.
    private func buildKOnlyQuadString(kValues: [UInt16]) -> String {
        var lines: [String] = []
        lines.append("## QuadToneRIP K,C,M,Y,LC,LM,LK,LLK")
        lines.append("# K curve")
        for v in kValues {
            lines.append("\(v)")
        }
        let zeroChannelNames = ["C", "M", "Y", "LC", "LM", "LK", "LLK"]
        for name in zeroChannelNames {
            lines.append("# \(name) Curve")
            for _ in 0..<256 {
                lines.append("0")
            }
        }
        return lines.joined(separator: "\n")
    }

    /// Generate a 256-line curve export string (integer values).
    private func generateCurveTxtString(gamma: Double = 1.0, maxValue: Int = 29695) -> String {
        var lines: [String] = []
        lines.append("# Curve export")
        lines.append("# Generated by CurveIOTests")
        for i in 0..<256 {
            let t = Double(i) / 255.0
            let v = pow(t, gamma)
            let intVal = Int(Double(maxValue) * v)
            lines.append("\(intVal)")
        }
        return lines.joined(separator: "\n")
    }

    /// Build a measurement .txt string with the given number of steps.
    private func buildMeasurementString(steps: Int) -> String {
        var lines: [String] = []
        lines.append("QuadToneProfiler Measurement Data File")
        lines.append("Step\tL*\ta*\tb*")
        for i in 0..<steps {
            let lStar = 95.0 - (Double(i) / Double(max(steps - 1, 1))) * 90.0
            let aStar = 0.1 + Double(i) * 0.03
            let bStar = 1.5 + Double(i) * 0.1
            lines.append("\(i + 1)\t\(String(format: "%.2f", lStar))\t\(String(format: "%.2f", aStar))\t\(String(format: "%.2f", bStar))")
        }
        return lines.joined(separator: "\n")
    }

