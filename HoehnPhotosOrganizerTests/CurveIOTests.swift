    func test_saveCurveVersions_differentKValues() throws {
        let names = ["MyProcess.quad", "MyProcess-v2.quad", "MyProcess-v3.quad"]
        let gammas = [1.0, 0.5, 2.0]  // linear, lighter, darker

        var parsedQuads: [QTRQuadFile] = []

        for (i, name) in names.enumerated() {
            let url = tempDir.appendingPathComponent(name)
            let kValues = generateGammaKChannel(gamma: gammas[i])
            let content = buildKOnlyQuadString(kValues: kValues)
            try content.write(to: url, atomically: true, encoding: .utf8)
            let parsed = try QTRFileParser.parseQuadFile(at: url)
            parsedQuads.append(parsed)
        }

        // Compare midpoint (index 128) values across versions
        let midLinear = parsedQuads[0].channels[0].values[128]
        let midLight = parsedQuads[1].channels[0].values[128]   // gamma 0.5 = lighter
        let midDark = parsedQuads[2].channels[0].values[128]    // gamma 2.0 = darker

        // gamma 0.5 midpoint should be higher than linear midpoint (more ink early)
        XCTAssertGreaterThan(midLight, midLinear, "Gamma 0.5 midpoint should be above linear")
        // gamma 2.0 midpoint should be lower than linear midpoint (less ink early)
        XCTAssertLessThan(midDark, midLinear, "Gamma 2.0 midpoint should be below linear")
    }

