    func test_serializeQuadFile_256ValuesPerChannel() throws {
        let quad = try QTRFileParser.parseQuadFile(
            content: testLinearKOnlyQuad,
            fileName: "test.quad"
        )
        let serialized = serializeQuadFile(quad)
        let lines = serialized.components(separatedBy: .newlines)

        // Count data lines per channel section
        var channelDataCounts: [Int] = []
        var currentCount = 0
        var inChannel = false

        for line in lines {
            let trimmed = line.trimmingCharacters(in: .whitespaces)
            if trimmed.hasPrefix("#") {
                let lower = trimmed.lowercased()
                // Match the production parser's channel marker detection:
                // must contain "curve", must NOT contain "quadtonerip", "linearized", or "max"
                if lower.contains("curve") && !lower.contains("quadtonerip")
                    && !lower.contains("linearized") && !lower.contains("max") {
                    if inChannel {
                        channelDataCounts.append(currentCount)
                    }
                    currentCount = 0
                    inChannel = true
                }
                continue
            }
            if inChannel && !trimmed.isEmpty && UInt16(trimmed) != nil {
                currentCount += 1
            }
        }
        // Flush last channel
        if inChannel {
            channelDataCounts.append(currentCount)
        }

        XCTAssertEqual(channelDataCounts.count, 8, "Should have 8 channel sections")
        for (i, count) in channelDataCounts.enumerated() {
            XCTAssertEqual(count, 256, "Channel \(i) should have 256 data lines, got \(count)")
        }
    }

    func test_serializeQuadFile_roundTrip() throws {
        let original = try QTRFileParser.parseQuadFile(
            content: testLinearKOnlyQuad,
            fileName: "test.quad"
        )

        // Serialize -> parse -> serialize -> parse
        let serialized1 = serializeQuadFile(original)
        let parsed1 = try QTRFileParser.parseQuadFile(content: serialized1, fileName: "test.quad")
        let serialized2 = serializeQuadFile(parsed1)
        let parsed2 = try QTRFileParser.parseQuadFile(content: serialized2, fileName: "test.quad")

        // Both parsed results should have identical channel values
        for chIdx in 0..<8 {
            let vals1 = parsed1.channels[chIdx].values
            let vals2 = parsed2.channels[chIdx].values
            XCTAssertEqual(vals1.count, vals2.count, "Channel \(chIdx) value count mismatch")
            for vIdx in 0..<vals1.count {
                XCTAssertEqual(
                    vals1[vIdx], vals2[vIdx],
                    "Channel \(chIdx) value mismatch at index \(vIdx): \(vals1[vIdx]) vs \(vals2[vIdx])"
                )
            }
        }
    }

    // MARK: - Production Serializer Round-Trip

    func test_productionSerializer_roundTrip_identicalChannelValues() throws {
        // Parse a known-good quad string
        let original = try QTRFileParser.parseQuadFile(
            content: testLinearKOnlyQuad,
            fileName: "roundtrip.quad"
        )

        // Serialize with the PRODUCTION serializer (QTRFileParser.serializeQuadFile)
        let serialized = QTRFileParser.serializeQuadFile(original)

        // Parse the serialized output
        let reparsed = try QTRFileParser.parseQuadFile(
            content: serialized,
            fileName: "roundtrip.quad"
        )

        // Assert identical channel values
        XCTAssertEqual(reparsed.channels.count, original.channels.count,
                       "Channel count should match after round-trip")
        for chIdx in 0..<original.channels.count {
            let origVals = original.channels[chIdx].values
            let newVals = reparsed.channels[chIdx].values
            XCTAssertEqual(origVals.count, newVals.count,
                           "Channel \(original.channels[chIdx].name) value count mismatch")
            for vIdx in 0..<origVals.count {
                XCTAssertEqual(
                    origVals[vIdx], newVals[vIdx],
                    "Channel \(original.channels[chIdx].name) value mismatch at index \(vIdx): " +
                    "expected \(origVals[vIdx]), got \(newVals[vIdx])"
                )
            }
        }
    }

    func test_productionSerializer_roundTrip_multiChannel() throws {
        // Build a quad with K + C active (two non-zero channels)
        let kValues = generateGammaKChannel(gamma: 1.0)
        let cValues = generateGammaKChannel(gamma: 0.8, maxValue: 20000)
        var channelArrays: [[UInt16]] = Array(repeating: Array(repeating: UInt16(0), count: 256), count: 8)
        channelArrays[0] = kValues
        channelArrays[1] = cValues

        let channelNames = ["K", "C", "M", "Y", "LC", "LM", "LK", "LLK"]
        let channels = channelArrays.enumerated().map { i, vals in
            InkChannel(name: channelNames[i], values: vals)
        }
        let quad = QTRQuadFile(
            fileName: "multi.quad",
            comments: ["## QuadToneRIP K,C,M,Y,LC,LM,LK,LLK"],
            channels: channels
        )

        // Serialize → parse → assert identical
        let serialized = QTRFileParser.serializeQuadFile(quad)
        let reparsed = try QTRFileParser.parseQuadFile(content: serialized, fileName: "multi.quad")

        for chIdx in 0..<8 {
            let origVals = quad.channels[chIdx].values
            let newVals = reparsed.channels[chIdx].values
            for vIdx in 0..<256 {
                XCTAssertEqual(
                    origVals[vIdx], newVals[vIdx],
                    "Channel \(channelNames[chIdx]) mismatch at \(vIdx)"
                )
            }
        }
    }

